    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
            try {
        // Open file reader
        BufferedReader reader = new BufferedReader(new FileReader("users.txt"));
        String line;
        boolean isLoggedIn = false;

        // Get user inputs
        String enteredUsername = jTextField1.getText().trim();
        String enteredPassword = new String(jPasswordField1.getPassword()).trim();

        // Read the file line by line
        while ((line = reader.readLine()) != null) {
            String[] parts = line.split(", ");
            if (parts.length < 4) continue; // Skip invalid lines

            String username = parts[0];
            String password = parts[1];
            String role = parts[2]; // Role will be determined here
            String id = parts[3];

            // Check if username and password match
            if (enteredUsername.equals(username) && enteredPassword.equals(password)) {
                isLoggedIn = true;

                // Show success message
                JOptionPane.showMessageDialog(this, "Login successful as " + role + " (ID: " + id + ")");

                // Switch to the appropriate home panel based on role
                frame.remove(this);
                switch (role.toLowerCase()) {
                    case "admin":
                        ChooseBalance.customerID = parts[3];
                        frame.add(new AdminHome(frame)); // Admin home panel
                        break;
                    case "customer":
                        customer customer = new customer();
                        customer.setUserID(parts[3]);
                        ChooseBalance.customerID = parts[3];
//                        frame.add(new cusMainPage(frame)); // Customer home panel
                        break;
                    case "manager":
                        frame.add(new ManagerHome(frame)); // Manager home panel
                        break;
                    case "vendor":
                        customer vendor = new customer();
                        vendor.setUserID(parts[3]);
                        //frame.add(new testing(frame)); // Vendor home panel
                        frame.add(new vpage(frame)); // Vendor home panel
//                        frame.add(new vpage(frame)); // Vendor home panel
                        break;
                    case "deliveryrunner":
                        runner runner = new runner();
                        runner.setUserID(parts[3]);
                        frame.add(new mainPage(frame)); // Delivery Runner home panel
                        break;
                    default:
                        JOptionPane.showMessageDialog(this, "Unknown role detected!", "Error", JOptionPane.ERROR_MESSAGE);
                        break;
                }
                frame.revalidate();
                frame.repaint();
                break; // Exit loop after successful login
            }
        }

        // If no match is found, show error
        if (!isLoggedIn) {
            JOptionPane.showMessageDialog(this, "Invalid username or password", "Error", JOptionPane.ERROR_MESSAGE);
        }

        reader.close(); // Close file reader
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton3ActionPerformed


